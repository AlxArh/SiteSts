        <h2 id="Submarines">Submarines</h2>
        <h3 id="The Iron Lung">The Iron Lung</h3>
        <p>The Iron Lung, also known as the SM-13, was a burner submarine created by the C.O.I for Conviction Realization and deep liquid photography and analysis on AT-5.</p>
        <h4>History</h4>
        <hr>
        <p>While it's said to be a submarine simply made for ocean exploring, it is intended as a punishment and execution for convicts, one of which is the player. Specifically, the punishment is that you are tasked with the dangerous job to take photographs of locations of interest in an anomaly known as the Blood Oceans. However, referencing the note found in the back of the submarine and the terminal relating to the SM-8, it seems that you were actually not meant to survive this task and the iron lung is a death trap in disguise, they don't care about your survival, all they want is pictures and then you'd get executed inside of the iron lung. The submarine is incredibly unsafe as pipes burst and components catch on fire easily, and even the slightest contact with an obstacle at a very slow speed will crack the hull and cause the submarine to get torn to pieces by the water. Its hull is noticeably weak as blood seeps in through cracks on the ceiling and the creatures in the ocean damage it with relative ease. The exterior could be a resemblance to the SM-8, however, the exit is welded shut, and escape from the submarine is impossible unless heavy equipment is to be used to open it. Considering the height of you and the ceiling of the Iron Lung and the length of the interior, the Iron Lung is relatively small in size, which is probably why it is nicknamed after the Iron Lung, a claustrophobic machine to help with breathing.</p>
        <img src="../assets/img/Bonez.jpg" alt="Iron Lung interior">
        <p id="imgdesc">Front of the Iron Lung's interior</p>
        <h4>Interior</h4>
        <hr>
        <p>The front of the Iron Lung possesses a caged light on the ceiling that dimly lights the cockpit, a porthole that in the intro gives a view under the blood ocean and eventually closes to prevent the hull from collapsing in on itself, a large rectangular analog meter on the left of the porthole that indicates the depth of the Iron Lung which slowly decreases in the intro, three smaller circular analog meters that serve an unknown purpose, a large rectangular meter that shows how much oxygen is left in the Iron Lung by having 4 vertical squares that light up and slowly decrease as you play the game, a desk that has a radar on the left with below it, a horizontal axis indicator that changes whenever you press the buttons below to turn the Iron Lung left or right, two coordinate indicators that show X and Y and changes whenever you press a button below that either moves the Iron Lung forward or backward, a fire extinguisher attached to the wall, and multiple pipes are seen on the sides that some have valves attached.</p>
        <br>
        <p>The back of the Iron Lung contains a terminal with heavily censored and monitored background information about conflicts and the rapture, the photography area, and four analog meters on the left that serve an unknown purpose. Inside the photography area is a monitor, a large rectangular horizontal button with lime lighting on the borders that if pressed will take a photograph of whatever is at the front of the Iron Lung and displays whatever is taken on the monitor, 26 analog meters that serves an unknown purpose, and a note right behind the partition on the ground and can easily be missed if most players prioritize gameplay than lore.</p>
        <br>
        <p>The note is located on the floor and reads as follows:</p>
        <p id="note">"This is not an expedition, it is an execution. When they put you in here, they don't want you to return. And even if you do, and even if they keep their promises... what freedom waits for you? A few dying ships in a sea of dead stars?<br>If there still is hope, it lies beyond the veil. Hope in this void is as illusionary as the starlight. I will choose to breathe my last here at the bottom of an ocean, unseen, unheard, uncontrolled.<br>They will get their execution.<br>I will get my freedom."</p>
        <p>The achievement description says that this was written by another convict, meaning this is not the iron lung's first expedition, also hinted by the terminal relating to the SM-8 as it shows that the person probably was left to die in the iron lung by suffocation.</p>
        <h4>Destruction</h4>
        <hr>
        <p>Ultimately, the fate of the Iron Lung isn't pretty as the damage taken from the scaly creature and the dense liquids of the blood ocean clattering against the weak hull of the iron lung is immense. A wound left suspected to be at the back of the Iron Lung allows blood to flow up to knee level due to the sudden defensive assault. The external damage allowed the frog creature to pry its way in, kill the player, and destroy all the components within leaving them scattered all across the blood ocean floor as described in the epilogue's monologue.</p>
